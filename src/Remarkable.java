import javax.swing.JOptionPane;

public class Remarkable {
public static void main(String[] args) {
	
String name = JOptionPane.showInputDialog("What is your name?" );	
	
	if (name.equalsIgnoreCase("Tate")) {
	JOptionPane.showMessageDialog(null, "Tate, you are a happy banana peel.");	
	}
	else if(name.equalsIgnoreCase("Andrew")) {
		JOptionPane.showMessageDialog(null, "Hello Andrew! You are great.");
	}
	else if(name.equalsIgnoreCase("Neal")) {
		JOptionPane.showMessageDialog(null, "Neal, you are a boss.");
	}
	else if(name.equalsIgnoreCase("Peter")) {
		JOptionPane.showMessageDialog(null, "Peter, you are a very awesome person.");
	}
	else if(name.equalsIgnoreCase("Sam")) {
		JOptionPane.showMessageDialog(null, "Hello Sam, you are a nice person.");
	}
	else if(name.equalsIgnoreCase("Ian")) {
		JOptionPane.showMessageDialog(null, "Hey Ian, why do you smell like cheese.");
	}
	
	
	
	else {
		JOptionPane.showMessageDialog(null, "Who are you?");
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}}
